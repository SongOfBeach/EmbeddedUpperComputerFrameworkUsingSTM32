 /**
  * @brief  配置嵌套向量中断控制器NVIC
  * @param  无
  * @retval 无
  */
static void NVIC_Configuration(void)
{
  NVIC_InitTypeDef NVIC_InitStructure;
  
  /* 嵌套向量中断控制器组选择 */
  NVIC_PriorityGroupConfig(NVIC_PriorityGroup_2);
  
  /* 配置USART为中断源 */
  NVIC_InitStructure.NVIC_IRQChannel = DEBUG_USART_IRQ;
  /* 抢断优先级*/
  NVIC_InitStructure.NVIC_IRQChannelPreemptionPriority = 1;
  /* 子优先级 */
  NVIC_InitStructure.NVIC_IRQChannelSubPriority = 1;
  /* 使能中断 */
  NVIC_InitStructure.NVIC_IRQChannelCmd = ENABLE;
  /* 初始化配置NVIC */
  NVIC_Init(&NVIC_InitStructure);
}

 /**
  * @brief  USART GPIO 配置,工作参数配置
  * @param  无
  * @retval 无
  */
void USART_Config(void)
{
	GPIO_InitTypeDef GPIO_InitStructure;
	USART_InitTypeDef USART_InitStructure;

	// 打开串口GPIO的时钟
	DEBUG_USART_GPIO_APBxClkCmd(DEBUG_USART_GPIO_CLK, ENABLE);
	
	// 打开串口外设的时钟
	DEBUG_USART_APBxClkCmd(DEBUG_USART_CLK, ENABLE);

	// 将USART Tx的GPIO配置为推挽复用模式
	GPIO_InitStructure.GPIO_Pin = DEBUG_USART_TX_GPIO_PIN;
	GPIO_InitStructure.GPIO_Mode = GPIO_Mode_AF_PP;
	GPIO_InitStructure.GPIO_Speed = GPIO_Speed_50MHz;
	GPIO_Init(DEBUG_USART_TX_GPIO_PORT, &GPIO_InitStructure);

  // 将USART Rx的GPIO配置为浮空输入模式
	GPIO_InitStructure.GPIO_Pin = DEBUG_USART_RX_GPIO_PIN;
	GPIO_InitStructure.GPIO_Mode = GPIO_Mode_IN_FLOATING;
	GPIO_Init(DEBUG_USART_RX_GPIO_PORT, &GPIO_InitStructure);
	
	// 配置串口的工作参数
	// 配置波特率
	USART_InitStructure.USART_BaudRate = DEBUG_USART_BAUDRATE;
	// 配置 针数据字长
	USART_InitStructure.USART_WordLength = USART_WordLength_8b;
	// 配置停止位
	USART_InitStructure.USART_StopBits = USART_StopBits_1;
	// 配置校验位
	USART_InitStructure.USART_Parity = USART_Parity_No ;
	// 配置硬件流控制
	USART_InitStructure.USART_HardwareFlowControl = 
	USART_HardwareFlowControl_None;
	// 配置工作模式，收发一起
	USART_InitStructure.USART_Mode = USART_Mode_Rx | USART_Mode_Tx;
	// 完成串口的初始化配置
	USART_Init(DEBUG_USARTx, &USART_InitStructure);
	
	// 串口中断优先级配置
	NVIC_Configuration();
	
	// 使能串口接收中断
	USART_ITConfig(DEBUG_USARTx, USART_IT_RXNE, ENABLE);	
	
	// 使能串口
	USART_Cmd(DEBUG_USARTx, ENABLE);	    
}

/*****************  发送一个字节 **********************/
void Usart_SendByte( USART_TypeDef * pUSARTx, uint8_t ch)
{
	/* 发送一个字节数据到USART */
	USART_SendData(pUSARTx,ch);
		
	/* 等待发送数据寄存器为空 */
	while (USART_GetFlagStatus(pUSARTx, USART_FLAG_TXE) == RESET);	
}

/****************** 发送8位的数组 ************************/
void Usart_SendArray( USART_TypeDef * pUSARTx, uint8_t *array, uint16_t num)
{
  uint8_t i;
	uint8_t ch;

/************* SLIP START ***************/
	Usart_SendByte(pUSARTx,__END__);
	for(i=0; i<num; i++)
  {
		
		if(array[i] == __ESC__){/*需要转义字符*/
			Usart_SendByte(pUSARTx,__ESC__);
			ch = 0xdd;
		}else if(array[i] == __END__){
			Usart_SendByte(pUSARTx,__ESC__);
			ch = 0xdc;
		}else{
			ch = array[i];
		}
	   /* 发送一个字节数据到USART */
	   Usart_SendByte(pUSARTx,ch);	
  }
	Usart_SendByte(pUSARTx,__END__);
/************** SLIP END ****************/
	/* 等待发送完成 */
	while(USART_GetFlagStatus(pUSARTx,USART_FLAG_TC)==RESET);
}
/*****************  接收8位数组 **********************/
int Usart_RevArray( USART_TypeDef * pUSARTx, uint8_t *array)
{
	int ch,cnt=0;
	int timeUp=0;
	bool flag_End=false,flag_esc=false;

	while(1){
		if(usartRevdFlag==1)
		{
			usartRevdFlag=0;
			timeUp=0;
			ch = /*(int)USART_ReceiveData(DEBUG_USARTx)*/usartRevData;
/************* SLIP START ***************/
			flag_esc = false;
			if(ch==__ESC__)/*有转义字符*/
			{
				flag_esc=true;
				array[cnt++] = ch;
			}
			else if(flag_esc==true)/*数据转义*/
			{
				cnt--;
				if(ch==0xdc){//有转义
					array[cnt++] = __END__;
				}else if(ch==0xdd){
					array[cnt++] = __ESC__;
				}else{/*无转义，表示数据*/
					array[++cnt] = ch;
					cnt++;
				}
			}
			else if(ch==__END__)/*数据开头结尾*/
			{
				flag_End = (bool)(!flag_End);
				if(!flag_End)break;
			}
			else/*接收数据*/
			{
				array[cnt++] = ch;
			}
/************** SLIP END ****************/
		}else{/*接收超时处理*/
			timeUp++;
			if(timeUp>0x1ffff) break;
		}
	}
	return cnt;
}

/*****************  发送字符串 **********************/
void Usart_SendString( USART_TypeDef * pUSARTx, char *str)
{
	unsigned int k=0;
  do 
  {
      Usart_SendByte( pUSARTx, *(str + k) );
      k++;
  } while(*(str + k)!='\0');
  
  /* 等待发送完成 */
  while(USART_GetFlagStatus(pUSARTx,USART_FLAG_TC)==RESET)
  {}
}

